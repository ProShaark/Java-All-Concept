package Java_Programs_clearn_concept.Java_Basic_Program;

public class FactorialProgram {

	public static void Example1()
	{ 
		int n=4;
		int temp=1;
		for(int i=1;i<=n;i++)
		{
			
			temp=temp*i;
				
			
		}
		
	System.out.println(temp);
	}
	


	
	public static void main(String[] args) {
		Example1();
	}
	
	
}
